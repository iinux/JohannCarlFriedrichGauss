    done
    echo "其他 ISP / 未知:         ${#api_other[@]} 个"
    (( ${#api_other[@]} )) && echo "  ${api_other[*]}"

    # 6. 结论
    echo
    echo "---------- 结论 ----------"
    if (( ${#matched_order[@]} == 0 )); then
        echo "→ 路径上未发现已收录骨干网节点（可能为其他运营商、本地局域网或国际出口）。"
    elif (( ${#matched_order[@]} == 1 )); then
        as_num="${matched_order[0]}"
        echo "→ 走 ${BACKBONE_DESC[$as_num]}"
    else
        echo "→ 混合: 路径上同时出现多个骨干网节点:"
        for as_num in "${matched_order[@]}"; do
            echo "   - ${BACKBONE_DESC[$as_num]} (${hit_total[$as_num]} 个)"
        done
    fi
    echo
done
